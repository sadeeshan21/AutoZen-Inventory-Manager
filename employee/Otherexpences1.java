    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
//save button

        try {
            // 1. Get form values
            String billId = txtbillid.getText().trim();
            String category = txtcategory.getSelectedItem().toString();
            String year = txtyear.getText().trim();
            int month = txtmonth.getMonth() + 1; // Assuming this returns int (0-11) int month = txtmonth.getMonth()+1; 
            BigDecimal amount = new BigDecimal(txtamount.getText().trim());
            String status = txtstatus.getSelectedItem().toString();

            // Basic input validation
            if (billId.isEmpty() || year.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please fill all required fields.");
                return;
            }

            // 2. Insert into 'otherbills' table
            String sql1 = "INSERT INTO otherbills (biiid, category, year, month, amount, status) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement pst = db.myccon().prepareStatement(sql1);
            pst.setString(1, billId);
            pst.setString(2, category);
            pst.setString(3, year);
            pst.setInt(4, month);
            pst.setBigDecimal(5, amount);
            pst.setString(6, status);

            pst.executeUpdate(); // ✅ Execute insert

            // 3. Insert into 'otherbillreport' table
            String sql2 = "INSERT INTO otherbillreport (billid, status, amount) VALUES (?, ?, ?)";
            PreparedStatement sst = db.myccon().prepareStatement(sql2);
            sst.setString(1, billId);
            sst.setString(2, status);
            sst.setBigDecimal(3, amount);

            sst.executeUpdate(); // ✅ Execute second insert

            JOptionPane.showMessageDialog(this, "Bill saved successfully!");

            // 4. Clear form fields
            txtbillid.setText("");
            txtcategory.setSelectedIndex(0);
            txtyear.setText("");
            txtmonth.setMonth(0); // reset month picker
            txtamount.setText("");
            txtstatus.setSelectedIndex(0);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid number entered. Please check the amount.");
        } catch (SQLIntegrityConstraintViolationException e) {
            JOptionPane.showMessageDialog(this, "Duplicate entry for Bill ID.");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

