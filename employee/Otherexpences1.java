    public void billtableload() {  //number 1
        try {
            DefaultTableModel dt = (DefaultTableModel) otherbill_table.getModel(); //load table and store to dt
            dt.setRowCount(0);  // Clear existing rows before loading new ones

            Statement s = db.myccon().createStatement();
            ResultSet rs = s.executeQuery("SELECT * FROM otherbills"); //execute quary

            while (rs.next()) {
                Vector v = new Vector();

                // Add each column value from the result set to the vector
                v.add(rs.getString("biiid"));
                v.add(rs.getString("category"));
                v.add(rs.getString("year"));
                v.add(rs.getString("month"));
                v.add(rs.getString("amount"));
                v.add(rs.getString("status"));

                // ✅ Add the vector as a row to the table model
                dt.addRow(v);
            }
            DefaultTableCellRenderer statusRenderer = new DefaultTableCellRenderer() {
                @Override
                public Component getTableCellRendererComponent(JTable table, Object value,
                        boolean isSelected, boolean hasFocus, int row, int column) {

                    Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);

                    if (value != null) {
                        String status = value.toString().toLowerCase();

                        if (status.equals("paid")) {
                            c.setForeground(Color.BLACK);
                        } else if (status.equals("unpaid")) {
                            c.setForeground(Color.RED);
                        } else {
                            c.setForeground(Color.BLACK);
                        }
                    }

                    if (isSelected) {
                        c.setBackground(table.getSelectionBackground());
                    } else {
                        c.setBackground(Color.WHITE);
                    }

                    return c;
                }
            };

            otherbill_table.getColumnModel().getColumn(5).setCellRenderer(statusRenderer);

        } catch (Exception e) {
            e.printStackTrace();  // Print error to the console
            JOptionPane.showMessageDialog(null, "Error loading data into table: " + e.getMessage());
        }
    }

