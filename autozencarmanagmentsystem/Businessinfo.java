    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked


        // TODO add your handling code here:
        
        
        try {
        String name = txtbizname.getText().trim();
        String ceo = txt.getText().trim();
        String contact = txtbizcontact.getText().trim();
        String email = txtmail.getText().trim();

        // Check for empty fields
        if (name.isEmpty() || ceo.isEmpty() || contact.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return;
        }

        // Prepare SQL update
        String sql = "UPDATE businessdetails SET businessname = ?, ceo = ?, contactnumber = ?, email = ? WHERE idnu = 1";

        PreparedStatement pst = db.myccon().prepareStatement(sql);
        pst.setString(1, name);
        pst.setString(2, ceo);
        pst.setString(3, contact);
        pst.setString(4, email);

        int rows = pst.executeUpdate();
        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "Business details updated successfully.");
        } else {
            JOptionPane.showMessageDialog(this, "Failed to update. Record not found.");
        }

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton2MouseClicked

