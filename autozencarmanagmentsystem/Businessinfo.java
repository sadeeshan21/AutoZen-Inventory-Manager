    public void loadBusinessDetails() {
    try {
        Statement s = db.myccon().createStatement();
        ResultSet rs = s.executeQuery("SELECT * FROM businessdetails WHERE idnu = 1");

        if (rs.next()) {
            txtbizname.setText(rs.getString("businessname"));
            txtbizceo.setText(rs.getString("ceo"));
            txtbizcontact.setText(rs.getString("contactnumber"));
            txtmail.setText(rs.getString("email"));
            
        } else {
            JOptionPane.showMessageDialog(this, "Business details not found.");
        }

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading details: " + e.getMessage());
    }
}

